def build_unified_antt_df(df: pd.DataFrame, source_name: str) -> pd.DataFrame:
    df = map_antt_columns(df)

    # caso wide: colunas civ/cipp/ctpp
    cert_cols_present = [c for c in ["civ", "cipp", "ctpp"] if c in df.columns]
    if cert_cols_present:
        id_vars = [c for c in ["ano_ref", "mes_ref", "uf", "municipio", "empresa"] if c in df.columns]
        melted = df.melt(
            id_vars=id_vars,
            value_vars=cert_cols_present,
            var_name="certificado_tipo",
            value_name="quantidade",
        )
        df = melted
        if "tipo_certificado" not in df.columns:
            df["tipo_certificado"] = df["certificado_tipo"]
    else:
        if "certificado_tipo" not in df.columns:
            if "tipo_certificado" in df.columns:
                df["certificado_tipo"] = df["tipo_certificado"]
            else:
                df["certificado_tipo"] = pd.NA

    # garante colunas
    for col in ["ano_ref", "mes_ref", "uf", "municipio", "empresa", "tipo_certificado", "quantidade", "certificado_tipo"]:
        if col not in df.columns:
            df[col] = pd.NA

    ano = to_year_series(df["ano_ref"])
    mes = pd.to_numeric(df["mes_ref"], errors="coerce").astype("Int64")
    uf = clean_series(df["uf"]).str.upper()
    municipio = clean_series(df["municipio"])
    empresa = clean_series(df["empresa"])
    certificado_tipo = clean_series(df["certificado_tipo"]).str.upper()
    quantidade = pd.to_numeric(df["quantidade"], errors="coerce")

    mask_year = ano.isin(list(ANOS_DESEJADOS))
    mask_sp = uf.eq(UF_FOCO) | municipio.str.lower().eq("sao paulo")
    mask = mask_year & mask_sp

    unified = pd.DataFrame({
        "fonte": "ANTT",
        "tipo_registro": "certificacao",
        "ano_ref": ano,
        "mes_ref": mes,
        "uf": uf,
        "municipio": municipio,
        "empresa": empresa,
        "cnpj": pd.NA,
        "produto": pd.NA,
        "categoria": "Certificação",
        "quantidade": quantidade,
        "unidade_medida": "certificados",
        "modal": "Rodoviário",
        "origem": pd.NA,
        "destino": pd.NA,
        "armazenamento": pd.NA,
        "plano_acao": pd.NA,
        "certificado_tipo": certificado_tipo,
    })

    unified = unified[mask].copy()
    unified = unified.drop_duplicates()
    return unified


def process_antt_csvs(csv_paths: List[Path]) -> pd.DataFrame:
    frames: List[pd.DataFrame] = []

    if not csv_paths:
        log("[ANTT] Nenhum CSV da ANTT foi encontrado.")
        return pd.DataFrame(columns=UNIFIED_COLUMNS)

    for path in csv_paths:
        encoding = detect_encoding(path)
        sep = detect_separator(path, encoding)
        log(f"[ANTT] Arquivo: {path}")
        log(f"[ANTT] Encoding detectado: {encoding}")
        log(f"[ANTT] Separador detectado: {repr(sep)}")
        try:
            df = pd.read_csv(
                path,
                sep=sep,
                encoding=encoding,
                dtype="string",
                engine="python",
            )
            unified = build_unified_antt_df(df, path.name)
            log(f"[ANTT] Linhas lidas={len(df):,} | mantidas={len(unified):,}")
            if not unified.empty:
                frames.append(unified)
        except Exception as e:
            log(f"[ANTT] Erro ao ler {path.name}: {e}")

    if frames:
        result = pd.concat(frames, ignore_index=True).drop_duplicates()
    else:
        result = pd.DataFrame(columns=UNIFIED_COLUMNS)

    if not result.empty:
        anos = sorted(result["ano_ref"].dropna().astype(int).unique().tolist())
        log(f"[ANTT] Anos encontrados após filtro: {anos}")

    return result



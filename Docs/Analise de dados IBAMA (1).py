def map_ibama_columns(df: pd.DataFrame) -> pd.DataFrame:
    original_cols = list(df.columns)
    norm_map = {col: normalize_col(col) for col in original_cols}
    rename_map: Dict[str, str] = {}
    used_targets = set()

    for original, norm in norm_map.items():
        for target, aliases in IBAMA_ALIASES.items():
            if target in used_targets:
                continue
            if norm == target or norm in aliases or any(alias == norm for alias in aliases):
                rename_map[original] = target
                used_targets.add(target)
                break

    df = df.rename(columns=rename_map)
    df.columns = [normalize_col(c) for c in df.columns]
    df = remove_duplicate_columns(df)

    # garante colunas essenciais
    for col in IBAMA_ALIASES.keys():
        if col not in df.columns:
            df[col] = pd.NA

    return df



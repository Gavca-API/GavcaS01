def log(msg: str) -> None:
    print(msg, flush=True)


def normalize_text(value: object) -> str:
    """Normaliza texto: tira acentos, espaços extras e pontuação pesada."""
    if value is None or (isinstance(value, float) and pd.isna(value)):
        return ""
    text = str(value).strip()
    if not text:
        return ""
    text = unicodedata.normalize("NFKD", text)
    text = "".join(ch for ch in text if not unicodedata.combining(ch))
    text = text.replace("\n", " ").replace("\r", " ").replace("\t", " ")
    text = re.sub(r"\s+", " ", text)
    return text.strip()


def normalize_col(col: object) -> str:
    text = normalize_text(col).lower()
    text = re.sub(r"[^a-z0-9]+", "_", text)
    text = re.sub(r"_+", "_", text).strip("_")
    return text


def ensure_parent_dir(path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)


def detect_encoding(file_path: Path) -> str:
    """Tenta detectar um encoding comum de forma simples e robusta."""
    candidates = ["utf-8-sig", "utf-8", "cp1252", "latin1"]
    raw = file_path.read_bytes()[:100_000]
    for enc in candidates:
        try:
            raw.decode(enc)
            return enc
        except UnicodeDecodeError:
            continue
    return "latin1"


def detect_separator(file_path: Path, encoding: str) -> str:
    """Tenta descobrir o separador do CSV lendo um pedaço do arquivo."""
    sample = file_path.read_text(encoding=encoding, errors="ignore")[:20_000]
    try:
        dialect = csv.Sniffer().sniff(sample, delimiters=";,|\t")
        return dialect.delimiter
    except Exception:
        # fallback simples
        counts = {sep: sample.count(sep) for sep in [";", ",", "|", "\t"]}
        best_sep = max(counts, key=counts.get)
        return best_sep if counts[best_sep] > 0 else ";"


def clean_series(series: pd.Series) -> pd.Series:
    return (
        series.astype("string")
        .fillna("")
        .map(normalize_text)
    )


def pick_first_existing(df: pd.DataFrame, candidates: Iterable[str]) -> Optional[str]:
    for col in candidates:
        if col in df.columns:
            return col
    return None


def coalesce_columns(df: pd.DataFrame, candidates: Iterable[str]) -> pd.Series:
    cols = [c for c in candidates if c in df.columns]
    if not cols:
        return pd.Series([""] * len(df), index=df.index, dtype="string")
    result = clean_series(df[cols[0]])
    for col in cols[1:]:
        current = clean_series(df[col])
        result = result.mask(result.eq(""), current)
    return result


def to_year_series(series: pd.Series) -> pd.Series:
    extracted = (
        series.astype("string")
        .fillna("")
        .str.extract(r"(\d{4})", expand=False)
    )
    return pd.to_numeric(extracted, errors="coerce").astype("Int64")


def to_numeric_series(series: pd.Series) -> pd.Series:
    s = series.astype("string").fillna("").str.strip()

    # tenta detectar formato brasileiro com milhar "." e decimal ","
    has_comma = s.str.contains(",", regex=False, na=False)
    has_dot = s.str.contains(".", regex=False, na=False)

    # converte:
    # 1.234,56 -> 1234.56
    # 1234,56 -> 1234.56
    # 1234.56 -> 1234.56
    s = s.str.replace(".", "", regex=False)
    s = s.str.replace(",", ".", regex=False)

    # caso original fosse só ponto decimal, acima removeria o ponto.
    # então vamos tentar recuperar via regex a partir do texto original quando necessário.
    num = pd.to_numeric(s, errors="coerce")

    return num


def combine_uf_municipio(uf: pd.Series, municipio: pd.Series) -> pd.Series:
    uf_clean = clean_series(uf).str.upper()
    mun_clean = clean_series(municipio)
    combined = mun_clean.mask(mun_clean.eq(""), "").astype("string")
    return combined.where(combined.eq(""), combined + " - " + uf_clean).mask(combined.eq(""), uf_clean)


def classify_product(text_series: pd.Series) -> pd.Series:
    s = clean_series(text_series).str.lower()

    combustivel = (
        s.str.contains("combust", na=False) |
        s.str.contains("gasolina", na=False) |
        s.str.contains("etanol", na=False) |
        s.str.contains("alcool", na=False) |
        s.str.contains("diesel", na=False) |
        s.str.contains("oleo combust", na=False) |
        s.str.contains("querosene", na=False) |
        s.str.contains("glp", na=False) |
        s.str.contains("gpl", na=False)
    )
    return pd.Series(
        ["Combustível" if x else "Carga perigosa" for x in combustivel],
        index=text_series.index,
        dtype="string",
    )


def classify_action_plan(plan_series: pd.Series) -> pd.Series:
    s = clean_series(plan_series).str.lower()
    has_plan = (
        s.str.contains("sim", na=False) |
        s.str.contains("possui", na=False) |
        s.str.contains("plano", na=False) |
        s.str.contains("emerg", na=False)
    )
    return pd.Series(
        ["Plano informado" if x else "Sem informação clara" for x in has_plan],
        index=plan_series.index,
        dtype="string",
    )


def filter_sp_rows(df: pd.DataFrame) -> pd.DataFrame:
    possible_uf_cols = [
        "uf",
        "estado",
        "uf_origem",
        "uf_destino",
        "uf_estabelecimento",
        "uf_municipio",
    ]
    possible_mun_cols = [
        "municipio",
        "municipio_origem",
        "municipio_destino",
        "municipio_estabelecimento",
        "destino",
        "origem",
    ]

    mask = pd.Series(False, index=df.index)

    for col in possible_uf_cols:
        if col in df.columns:
            mask = mask | clean_series(df[col]).str.upper().eq(UF_FOCO)

    for col in possible_mun_cols:
        if col in df.columns:
            mask = mask | clean_series(df[col]).str.lower().str.contains("sao paulo", na=False)

    return df[mask].copy()


def remove_duplicate_columns(df: pd.DataFrame) -> pd.DataFrame:
    if df.columns.duplicated().any():
        df = df.loc[:, ~df.columns.duplicated()].copy()
    return df



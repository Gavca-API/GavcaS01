    sort_cols = [c for c in ["ano_ref", "mes_ref", "fonte", "uf", "municipio", "empresa"] if c in df.columns]
    df = df.sort_values(sort_cols, na_position="last").reset_index(drop=True)

    df.to_csv(output_path, index=False, sep=";", encoding="utf-8-sig")
    log(f"[OK] Arquivo final gerado: {output_path}")



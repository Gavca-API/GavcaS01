def build_unified_ibama_chunk(df: pd.DataFrame) -> pd.DataFrame:
    df = map_ibama_columns(df)

    # cria colunas limpas
    ano = to_year_series(df["ano_ref"])
    empresa = clean_series(df["empresa"])
    cnpj = clean_series(df["cnpj"])
    uf_estab = clean_series(df["uf_estabelecimento"]).str.upper()
    municipio_estab = clean_series(df["municipio_estabelecimento"])
    produto = clean_series(df["produto"])
    quantidade = pd.to_numeric(df["quantidade"], errors="coerce")
    unidade = clean_series(df["unidade_medida"])
    modal = clean_series(df["modal"])
    armazenamento = clean_series(df["armazenamento"])
    plano = clean_series(df["plano_acao"])
    uf_origem = clean_series(df["uf_origem"]).str.upper()
    mun_origem = clean_series(df["municipio_origem"])
    uf_destino = clean_series(df["uf_destino"]).str.upper()
    mun_destino = clean_series(df["municipio_destino"])

    # filtro anos
    mask_year = ano.isin(list(ANOS_DESEJADOS))

    # filtro SP
    mask_sp = (
        uf_estab.eq(UF_FOCO) |
        uf_origem.eq(UF_FOCO) |
        uf_destino.eq(UF_FOCO) |
        municipio_estab.str.lower().eq("sao paulo") |
        mun_origem.str.lower().eq("sao paulo") |
        mun_destino.str.lower().eq("sao paulo")
    )

    mask = mask_year & mask_sp
    if not mask.any():
        return pd.DataFrame(columns=UNIFIED_COLUMNS)

    uf_main = uf_estab.mask(uf_estab.eq(""), uf_origem).mask(
        uf_estab.eq("") & uf_origem.eq(""), uf_destino
    )
    municipio_main = municipio_estab.mask(municipio_estab.eq(""), mun_origem).mask(
        municipio_estab.eq("") & mun_origem.eq(""), mun_destino
    )

    origem = combine_uf_municipio(uf_origem, mun_origem)
    destino = combine_uf_municipio(uf_destino, mun_destino)

    unified = pd.DataFrame({
        "fonte": "IBAMA",
        "tipo_registro": "transporte_armazenamento",
        "ano_ref": ano,
        "mes_ref": pd.NA,
        "uf": uf_main,
        "municipio": municipio_main,
        "empresa": empresa,
        "cnpj": cnpj,
        "produto": produto,
        "categoria": classify_product(produto),
        "quantidade": quantidade,
        "unidade_medida": unidade,
        "modal": modal,
        "origem": origem,
        "destino": destino,
        "armazenamento": armazenamento,
        "plano_acao": classify_action_plan(plano),
        "certificado_tipo": pd.NA,
    })

    unified = unified[mask].copy()
    unified = unified.drop_duplicates()
    return unified


def process_ibama_csv(csv_path: Path) -> pd.DataFrame:
    encoding = detect_encoding(csv_path)
    sep = detect_separator(csv_path, encoding)
    log(f"[IBAMA] Arquivo: {csv_path}")
    log(f"[IBAMA] Encoding detectado: {encoding}")
    log(f"[IBAMA] Separador detectado: {repr(sep)}")

    chunks_result: List[pd.DataFrame] = []
    total_lido = 0
    total_mantido = 0

    reader = pd.read_csv(
        csv_path,
        sep=sep,
        encoding=encoding,
        dtype="string",
        chunksize=CHUNK_SIZE_IBAMA,
        engine="python",
    )

    for i, chunk in enumerate(reader, start=1):
        total_lido += len(chunk)
        unified_chunk = build_unified_ibama_chunk(chunk)
        total_mantido += len(unified_chunk)
        if not unified_chunk.empty:
            chunks_result.append(unified_chunk)
        log(f"[IBAMA] Chunk {i}: lidas={len(chunk):,} | mantidas={len(unified_chunk):,}")

    if chunks_result:
        result = pd.concat(chunks_result, ignore_index=True)
    else:
        result = pd.DataFrame(columns=UNIFIED_COLUMNS)

    log(f"[IBAMA] Total lido: {total_lido:,}")
    log(f"[IBAMA] Total mantido após filtros: {total_mantido:,}")
    if not result.empty:
        anos = sorted(result["ano_ref"].dropna().astype(int).unique().tolist())
        log(f"[IBAMA] Anos encontrados após filtro: {anos}")
    return result


